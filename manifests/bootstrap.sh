#!/bin/sh
set -e -x

echo "----------------------------------------------------------------"
echo "Puppet - begin...."
echo "----------------------------------------------------------------"

if which puppet > /dev/null ; then
	echo "----------------------------------------------------------------"
	echo "Puppet is already installed."
	echo "----------------------------------------------------------------"
	exit 0
fi

echo "----------------------------------------------------------------"
echo "Installing puppet..."
echo "----------------------------------------------------------------"
sudo apt install -y puppet-common
echo ""
echo "----------------------------------------------------------------"
echo "Puppet installed. Finish."
echo "----------------------------------------------------------------"
